void test_twofish() {
  Twofish_initialise();

  Twofish_Byte key[] = "password";
  Twofish_Byte plain[] = "alice to bob man";
  Twofish_Byte *cipher = malloc(16);
  Twofish_Byte *decrypted = malloc(16);

  Twofish_key xkey;

  Twofish_prepare_key(key, strlen(key), &xkey);
  Twofish_encrypt(&xkey, plain, cipher);
  Twofish_decrypt(&xkey, cipher, decrypted);

  int rc = mem_isequal(plain, decrypted, sizeof(plain));
  TEST_CHECK(rc == 0);

  free(cipher);
  free(decrypted);
}

